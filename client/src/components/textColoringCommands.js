/**
* Функции, меняющие выделенную область текста
*/

function changeColor(state, api, color) {
    if (state.selectedText) {
        const currentText = state.selectedText
        console.log(currentText)
        let modifyText = ""
        if (currentText.indexOf("span") >= 0 && currentText.split("")[0] === "<") {
            const index0 = currentText.indexOf('"', 0)
            const index1 = currentText.indexOf('"', index0 + 1)
            console.log(index0, index1)
            modifyText = currentText.slice(0, index0 + 1) + "own_" + color + currentText.slice(index1)
        } else {
            const colorClassName = "own_" + color
            modifyText = `<span class="${colorClassName}">${state.selectedText}</span>`;
        }
        api.replaceSelection(modifyText);
    }
}

